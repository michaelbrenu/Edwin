"""
src/reports/report_generator.py
================================
Text-to-Campaign report generator for IWMRO.

This module consumes the structured JSON metadata produced by the
classifier + route optimiser pipeline and produces:

  1. A community education text block (plain text) — ready for Canva.
  2. An HTML summary report — renderable in a browser or embedded in
     Power BI as a web visual.
  3. A JSON campaign payload — for posting to social channels or an
     SMS gateway (e.g. Hubtel for Ghana).

Design rationale
----------------
•  Template-driven (Jinja2) so non-developers can edit wording without
   touching Python code.
•  Locale-aware placeholder for Twi/Ga translations (future sprint).
•  Zero external API calls — works fully offline on a field laptop.

Scalability
-----------
For 1 000+ bins, the aggregation step uses a streaming groupby over
the CSV export (pandas chunked reader) to avoid loading all records
into memory at once.
"""

