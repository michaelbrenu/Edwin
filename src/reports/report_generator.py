# ── Action lookup for HTML report ─────────────────────────────────────────────
ACTIONS = {
    "plastic":    "Promote sachet-water reduction + Kaneshie recycling drop-off",
    "organic":    "Distribute compost bins + enforce market waste segregation",
    "metal":      "Coordinate Tema scrap buyback + industrial partner collection",
    "electronic": "Schedule Tema E-Waste Facility drop-offs + bulk business collection",
}


# ── Aggregation helpers ────────────────────────────────────────────────────────
def _aggregate(bins: list[dict], route_summary: list[dict]) -> dict:
    total_bins     = len(bins)
    high_priority  = sum(1 for b in bins if b.get("collection_priority") == "high")
    type_counts    = Counter(b["waste_type"] for b in bins)
    dominant_type  = type_counts.most_common(1)[0][0] if type_counts else "plastic"
    waste_pct      = {
        wtype: round(count / total_bins * 100, 1)
        for wtype, count in type_counts.items()
    }
    total_km       = round(sum(r.get("total_km", 0) for r in route_summary), 2)

    # Hotspot zones
    zone_fills: dict[str, list] = defaultdict(list)
    zone_types: dict[str, list] = defaultdict(list)
    for b in bins:
        zone_fills[b["location_name"]].append(b["fill_level"])
        zone_types[b["location_name"]].append(b["waste_type"])

    zone_info: dict[str, object] = {}
    for zone in zone_fills:
        fills  = zone_fills[zone]
        types  = zone_types[zone]
        mfill  = sum(fills) / len(fills)
        dom    = Counter(types).most_common(1)[0][0]
        zone_info[zone] = type("ZoneInfo", (), {
            "total_bins":      len(fills),
            "mean_fill_level": round(mfill, 3),
            "dominant_type":   dom,
            "is_hotspot":      mfill >= 0.75,
        })()

    hotspot_zones = [z for z, info in zone_info.items() if info.is_hotspot]

    return {
        "total_bins":     total_bins,
        "high_priority":  high_priority,
        "high_pct":       round(high_priority / max(total_bins, 1) * 100, 1),
        "truck_count":    len(route_summary),
        "total_km":       total_km,
        "waste_breakdown": waste_pct,
        "dominant_type":  dominant_type,
        "hotspot_zones":  hotspot_zones,
        "zone_info":      zone_info,
        "route_summary":  route_summary,
        "actions":        ACTIONS,
        "report_date":    datetime.now().strftime("%d %B %Y, %H:%M"),
        "next_collection": (
            datetime.now().strftime("%-d %B %Y") if False  # force else branch
            else "48 hours from report generation"
        ),
        "footer": CAMPAIGN_FOOTER,
    }


# ── Public API ────────────────────────────────────────────────────────────────
def generate_reports(
    bins: list[dict],
    route_summary: list[dict],
    out_dir: Optional[Path] = None,
) -> dict[str, Path]:
    """
    Generate all report formats and write to disk.

    Parameters
    ----------
    bins          : list of annotated bin dicts (post-pipeline)
    route_summary : list of truck route summaries from route_optimizer
    out_dir       : output directory (defaults to data/exports)

    Returns
    -------
    dict mapping format name ->file path
    """
    out_dir = out_dir or DIRS["exports"]
    ctx     = _aggregate(bins, route_summary)
    env     = Environment(loader=BaseLoader())

    outputs: dict[str, Path] = {}

    # ── Plain text ────────────────────────────────────────────────────────
    plain_tmpl = env.from_string(_PLAIN_TEXT_TEMPLATE)
    plain_text = plain_tmpl.render(**ctx)
    plain_path = out_dir / "campaign_report.txt"
    plain_path.write_text(plain_text, encoding="utf-8")
    outputs["text"] = plain_path
    logger.info("Plain-text report ->%s", plain_path)

    # ── HTML ─────────────────────────────────────────────────────────────
    html_tmpl  = env.from_string(_HTML_TEMPLATE)
    html_text  = html_tmpl.render(**ctx)
    html_path  = out_dir / "campaign_report.html"
    html_path.write_text(html_text, encoding="utf-8")
    outputs["html"] = html_path
    logger.info("HTML report ->%s", html_path)

    # ── JSON campaign payload ─────────────────────────────────────────────
    json_payload = {
        "report_date":     ctx["report_date"],
        "total_bins":      ctx["total_bins"],
        "high_priority":   ctx["high_priority"],
        "dominant_type":   ctx["dominant_type"],
        "hotspot_zones":   ctx["hotspot_zones"],
        "waste_breakdown": ctx["waste_breakdown"],
        "total_km":        ctx["total_km"],
        "message":         plain_text,
    }
    json_path = out_dir / "campaign_payload.json"
    json_path.write_text(json.dumps(json_payload, indent=2), encoding="utf-8")
    outputs["json"] = json_path
    logger.info("JSON campaign payload ->%s", json_path)

    return outputs
